import math
import numpy

# RUN help(math) TO GET ALL FUNCTIONS AND MEMBERS

value = 4.35
print(value)
print(math.floor(value))
print(math.ceil(value))
print(round(4.5))
print(round(5.5))

# GETS THE CONSTANT VALUE PI
print(math.pi)

# PRINTS INFINITY
print(math.inf)

# PRINTS NOT A NUMBER
print(math.nan)

# PRINTS EULER'S CONSTANT
print(math.e)

# PRINTS LOG OF EULER'S CONSTANT
print(math.log(math.e))

# PRINTS CUSTOM LOG BASE
print(math.log(100, 10))
